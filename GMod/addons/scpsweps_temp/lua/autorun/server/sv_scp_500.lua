hook.Add("Initialize", "StartSpawningSCP500", function()
	timer.Simple(5, function()
		local scp500entity = ents.Create("scp_500")
		scp500entity:SetPos(Vector(-5246.980469, -2154.282959, -7218.768555 ))
		scp500entity:Spawn()
		timer.Create("RespawnSCP500", 900, 0, function()
			local scp500entity = ents.Create("scp_500")
			scp500entity:SetPos(Vector(-5246.980469, -2154.282959, -7218.768555 ))
			scp500entity:Spawn()
		end)
	end)
end)